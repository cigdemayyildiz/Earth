package PrimitiveDataTypes;

public class Practice1 {

    public static void main(String[] args) {

        /*
        Create 3 variables to store your age, height, and weight and print them using println.
        myAge, myHeight and myWeight
        */

        int myAge = 30;
        int myWeight = 52;
        float myHeight = 1.65f;

        System.out.println("My age is " + myAge + ", weight is " + myWeight + " kg and, height is " + myHeight + "m.");
        // My age is 30, weight is 52kg and, height is 1.65m.

    }
}
