package Loops;

public class DoWhileLoops2 {

    public static void main(String[] args) {

        int a = 15;
        int b = 15;

        do {
            System.out.println("This is DO WHILE example");

        }while (a<b);


        do {
            System.out.println("This is second DO WHILE example");
            a++;
        }while (a<=b);


        int x = 3;
        int y = 6;

        do {
            System.out.println("This is third DO WHILE example");
            x++;
        }while (x<y);

    }
}
